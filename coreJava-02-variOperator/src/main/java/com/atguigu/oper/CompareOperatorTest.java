package com.atguigu.oper;

public class CompareOperatorTest {
    public static void main(String[] args) {
        int m1 = 10;
        int m2 = 20;

        System.out.println("m1 = " + m1);
        System.out.println("m2 = " + m2);
        System.out.println("(m1 > m2) = " + (m1 > m2));

        System.out.println("(m1 == m2) = " + (m1 == m2));
        System.out.println("(m1 = m2) = " + (m1 = m2));
    }
}
