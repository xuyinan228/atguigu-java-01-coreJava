package com.atguigu.vari;

public class VariableTest {
    public static void main(String[] args) {
        // 变量的声明
        char gender;
        // 变量的赋值（初始化）
        gender = '男';

        // 声明与初始化合并
        int age = 20;

        System.out.println("gender = " + gender);
        System.out.println("age = " + age);
    }
}
